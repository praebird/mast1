class ArCr {
	private double radius;
	private double area;
	
	
	
	
	void Area( double r) {
	
	radius=r;
	area=3.14*radius*radius;
	System.out.println("Area of Circle="+area);
			
	}
	
	void Circumference( double r) {
		
	radius=r;
	area=3.14*2*radius;
	System.out.println("Circumference of Circle="+area);
			
	}
}
